    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) tblEdit.getModel();
        ArrayList<FlightDetails> list = FlightDetailsDb.getAllRecords();
        Iterator<FlightDetails> itr = list.iterator();
        while (itr.hasNext()) {
            FlightDetails flightDetailsObj = itr.next();
            dtm.addRow(new Object[]{flightDetailsObj.getId(), flightDetailsObj.getFlightName(), flightDetailsObj.getFlightNo(), flightDetailsObj.getPrice()});
        }
    }//GEN-LAST:event_formComponentShown

    private void tblEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEditMouseClicked
        // TODO add your handling code here:
        int index = tblEdit.getSelectedRow();
        TableModel model = tblEdit.getModel();
        String id = model.getValueAt(index, 0).toString();
        lblID.setText(id);
        String flightName = model.getValueAt(index, 1).toString();
        txtFlight.setText(flightName);
        String flightNo = model.getValueAt(index, 2).toString();
        String price = model.getValueAt(index, 3).toString();
        txtPrice.setText(price);

        btnUpdate.setEnabled(true);
        btnDelete.setEnabled(true);
        cbFlightNo.removeAllItems();
        cbFlightNo.addItem(flightNo);
        ArrayList<Flight> flightList = FlightDb.getallRecords();
        Iterator<Flight> flightItr = flightList.iterator();
        while (flightItr.hasNext()) {
            Flight flightObj = flightItr.next();
            if (!flightObj.getFlightName().equals(flightNo)) {
                cbFlightNo.addItem(flightObj.getFlightName());
            }
        }

    }//GEN-LAST:event_tblEditMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String id = lblID.getText();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to delete this record?", "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            FlightDetailsDb.delete(id);
            setVisible(false);
            new EditFlight().setVisible(true);
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

