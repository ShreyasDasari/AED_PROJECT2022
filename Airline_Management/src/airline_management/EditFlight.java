    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        FlightDetails flightDetails = new FlightDetails();
        int id = Integer.parseInt(lblID.getText());
        flightDetails.setId(id);
        flightDetails.setFlightName(txtFlight.getText());
        flightDetails.setFlightNo((String) cbFlightNo.getSelectedItem());
        flightDetails.setPrice(txtPrice.getText());
        FlightDetailsDb.update(flightDetails);
        setVisible(false);
        new EditFlight().setVisible(true);
    }//GEN-LAST:event_btnUpdateActionPerformed

