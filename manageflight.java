    private void tblFlightsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFlightsMouseClicked
        // TODO add your handling code here:
        int index = tblFlights.getSelectedRow();
        TableModel model = tblFlights.getModel();
        String id = model.getValueAt(index,0).toString();
        String flightName = model.getValueAt(index,1).toString();
        String flightNo = model.getValueAt(index,2).toString();
        String source = model.getValueAt(index, 3).toString();
        String destination = model.getValueAt(index, 4).toString();
        int a = JOptionPane.showConfirmDialog(null, "Do you really want to delete '"+flightName+"' and '"+flightNo+"' record?","Select",JOptionPane.YES_NO_OPTION);
        if (a==0){
            FlightDb.delete(id);
            setVisible(false);
            new ManageFlight().setVisible(true);
        }

    }//GEN-LAST:event_tblFlightsMouseClicked

