    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Flight flight = new Flight();
        Flight flightNo = new Flight();
        Flight source = new Flight();
        Flight destination = new Flight();
        flight.setFlightName(txtNewFlight.getText());
        flightNo.setFlightNo(txtFlightNo.getText());
        source.setSource(txtSource.getText());
        destination.setDestination(txtDestination.getText());
        FlightDb.save(flight, flightNo, source, destination);
        setVisible(false);
        new ManageFlight().setVisible(true);
    }//GEN-LAST:event_btnSaveActionPerformed

